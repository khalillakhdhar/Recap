public class SimpleClassconstruction {
    private int a,b,c;
    private String titre;

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }

    public int getC() {
        return c;
    }

    public void setC(int c) {
        this.c = c;
    }

    public String getTitre() {
        return titre;
    }

    public void setTitre(String titre) {
        this.titre = titre;
    }

    public SimpleClassconstruction() {
    }

    @Override
    public String toString() {
        return "SimpleClassconstruction{" + "a=" + a + ", b=" + b + ", c=" + c + ", titre=" + titre + '}';
    }
    
    
    
    
    
}
